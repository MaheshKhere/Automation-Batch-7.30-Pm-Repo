package decision_making_Statement;

public class Switch_Statement_001 {

	public static void main(String[] args) {
		
		int i = 25;
		switch (i) {
		case 25: 
			System.out.println("Case-1");
			break;
		case 20:
			System.out.println("Case-25");
			break;
		case 29 :
			System.out.println("Case-30");
			break;
		case 6:
			System.out.println("case-6");
			break;
			
			default :
				System.out.println("Your all cases is false");
				break;
		}

	}

}
