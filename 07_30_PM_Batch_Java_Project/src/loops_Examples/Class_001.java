package loops_Examples;

public class Class_001 {

	public static void main(String[] args) {
		// ++i
//		
		for (int i = 10 ; i >= 0; i--) {
			System.out.println(i);
		}

		// Print first 10 even numbers
		for (int x = 0; x<10;  x++) {
			System.out.println("The Even num:- " +( 2*x));
		}
	
	for(int x= 0; x<=100; x++) {
		if(x % 2 != 0) {
			System.out.println(x);	
		}
	}
	
	
	}

}
